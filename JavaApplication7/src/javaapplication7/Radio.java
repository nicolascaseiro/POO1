public class Radio {

    int hora;
    int minuto;
    int segundo;
    int horaAlarme;
    int minutoAlarme;

    public void setHora(int h) {
        hora = h;
    }

    public int getHora() {
        return hora;
    }

    public void setMinuto(int m) {
        minuto = m;
    }

    public int getMinuto() {
        return minuto;
    }

    public void setSegundo(int s) {
        segundo = s;
    }

    public int getSegundo() {
        return segundo;
    }

    public void setHoraAlarme(int ha) {
        horaAlarme = ha;
    }

    public int getHoraAlarme() {
        return horaAlarme;
    }

    public void setMinutoAlarme(int ma) {
        minutoAlarme = ma;
    }

    public int getMinutoAlarme() {
        return minutoAlarme;
    }
}
